package springFramework.springBoot;

public class Introducao {

	public static void main(String[] args) {
		/*
		 * Spring Framework é baseado no padrão de injeção de dependências,
		 * enquanto o Springboot foca na configuração automática.
		 * 
		 * Antes do boot, os desafios eram as dependências individuais, a verbosidade
		 * a incompatibilidade de versões, a complexidade de gestão, além da
		 * configuração complexa e repetitiva.
		 * 
		 * Dado que a maior parte das configurações necessárias para o inicio de um projeto
		 * são sempre as mesmas, ele inicia um projeto com todas as configurações necessárias
		 * já incluídas.
		 * 
		 * Starters: Descritores de dependência.
		 * Benefícios de Starters é a Coesão, Versões Compatíveis, Otimização do tempo,
		 * Configuração simples e Foco no negócio.
		 * Alguns Starters: data-jpa (integração ao banco de dados via JPA - Hibernate.), data-mongodb (integração
		 * com banco de dados MongoDB), web (inclusão de container Tomcat para aplicações REST),
		 * web-services: Webservices baseados na arquitetura SOAP.
		 * batch, test, openfeign e actuator são alguns outros.
		 * 
		 * 
		 */

	}

}
