package springFramework;

public class Introducao {

	public static void main(String[] args) {
		/*
		 * Spring possui módulos, como o Data Access/Integration (JDBC,
		 * ORM, OXM, JMS, Transactions), o Web (Web, Servlet, Portlet,
		 * Struts), o AOP, o Core Container (Beans, Core, Context, Expression
		 * Language) e o de Testes.
		 * 
		 * Spring foi um movimento por conta da Java EE.
		 * Java EE era complicado e o Spring era um caminho mais simples e 
		 * fácil de evoluir.
		 * 
		 * Inversão de Controle é o redirecionamento do fluxo de execução
		 * de um código, retirando parcialmente o controle sobre ele e delegando
		 * para um container.
		 * O principal motivo é minimzar o acoplamento do código.
		 * 
		 * Injeção de Dependências é um padrão de desenvolvimento com a finalidade
		 * de manter baixo o nível de acoplamento entre módulos de um sistema.
		 * 
		 * Beans é um objeto que é instaciado (criado), montado e gerenciado por um
		 * container por meio do princípio da inversão de controle.
		 * 
		 * Scopes existem 5 tipos, como o Singleton e Prototype (Padrões) e 
		 * os HTTP (Request, Session e Global).
		 * Singleton: Define apenas uma instância do objeto.
		 * Prototype: Vai ser criado um novo objeto a cada solicitação do container.
		 * Request: Bean criado para cada requisição HTTP. Existirão enquanto a requisição estiver em execução.
		 * Session: Bean criado para cada sessão de usuário.
		 * Global: Cria um bean para o ciclo de vida do contexto da aplicação.
		 * Autowired: uma anotação onde deve ocorrer uma injeção automática de dependencia.
		 * 
		 */

	}

}
