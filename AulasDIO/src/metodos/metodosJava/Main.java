package metodos.metodosJava;

public class Main {

	public static void main(String[] args) {
		/*
		 * É uma porção de código que é disponibilizada por uma classe.
		 * É executado quando feita uma requisição a ele e são responsáveis
		 * por definir e realizar um determinado comportamento.
		 * 
		 * Padrão de definição:
		 * Visibilidade ; Tipo ; Modificador ; retorno nome (parâmetros)
		 * exceções; corpo.
		 * 
		 * Visibilidade: public, protected ou private
		 * Tipo: concreto ou abstrato
		 * Modificador: static ou final
		 * Retorno: tipo de dado primitivo/objeto ou classe ou void
		 * Nome: nome que é fornecido ao método
		 * Parâmetros: parâmetros que pode receber
		 * Exceções: exceções que pode lançar
		 * Corpo: código que possui ou vazio
		 * 
		 * Particularidades:
		 * Assinatura é a forma de identificar unicamente o método.
		 * Ass = nome + parâmetros.
		 * 
		 * Método:
		 * public double calcularTotalVenda (precoItem1, double precoItem2, double precoItem3){...}
		 * Assinatura:
		 * calcularTotalVenda (double precoItem1, double precoItem2, double precoItem3){...}
		 * 
		 * Construtora e Destrutor: são métodos especiais usados na Orientação a Objetos.
		 * 
		 * Mensagem: é o ato de solicitar ao método que o mesmo execute. Este pode ser direcionada a um
		 * objeto ou a uma classe.
		 * 
		 * Passagem de parâmetros:
		 * Por valor (cópia) ou Por referência (endereço)
		 * 
		 * Boas práticas:
		 * Nomes devem ser descritivos, mas curtos; Notação camelo;
		 * Deve possuir entre 80 e 120 linhas; Evite lista de parâmetros longas;
		 * Visibilidades adequadas.
		 */

	}

}
