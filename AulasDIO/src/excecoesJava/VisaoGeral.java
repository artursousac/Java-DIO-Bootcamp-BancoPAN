package excecoesJava;

public class VisaoGeral {

	public static void main(String[] args) {
		/*
		 * Exceção é um evento que interrompe o fluxo normal do
		 * processamento de uma classe.
		 * Com o tratamento de exceções, um programa pode continuar executando
		 * depois de lidar com um problema.
		 * Pode ser mais complicado incluir um tratamento de exceções
		 * eficiente depois que um sistema foi implementado.
		 * 
		 * Error: Usado pela JVM que serve para indicar se existe
		 * algum problema de recurso do programa, tornando a execução
		 * impossível de continuar.
		 * 
		 * Unchecked: Exceptions que PODEM ser evitados se forem tratados
		 * e analisados pelo desenvolvedor.
		 * São exceções que perpetuam de erros de lógicas.
		 * 
		 * Checked Exception: Exceptions que DEVEM ser evitados e tratados
		 * pelo desenvolvedor para o programa funcionar.
		 * Não é possível nem rodar o programa caso possua Checked Exception.
		 * 
		 * RunTime exception não é checada pelo compilador e, normalmente,
		 * é erro de lógica.
		 * 
		 * try, catch, finally: Juntas, definem blocos para o tratamento de exceções.
		 * throws: Declara que um método pode lançar uma ou várias exceções.
		 * throw: Lança explicitamente uma exception.
		 * 
		 * 
		 */

	}

}
