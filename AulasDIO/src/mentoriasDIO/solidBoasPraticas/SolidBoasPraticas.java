package mentoriasDIO.solidBoasPraticas;

public class SolidBoasPraticas {

	public static void main(String[] args) {
		/*
		 * -> Código Limpo (Clean Code): Código facil de ler, entender e manter.
		 * O objetivo é facilitar a colaboração entre os desenvolvedores e
		 * garantir que o código possa ser modificado e atualizado com segurança
		 * 
		 * 1 - Legibilidade. 2 - Padrões. 3 - Separar configurações de código fonte.
		 * 4 - Evitar repetições. 5 - Cuidado com o uso de comentários. 6 - Tratamento de erros.
		 * 7 - Desenvolver testes limpos (Rápidos, independentes, passíveis de validação para um teste objetivo
		 * e pontual).
		 * 
		 * -> SOLID: É um acrônimo baseado nos 5 principios da POO.
		 * Ajuda a tornar o código mais limpo e fácil de manter.
		 * 
		 * S = Single Responsibility Principle.
		 * Este principio afirma que uma classe deve ter somente uma responsabilidade.
		 * Facilidade de manutenção; Código Limpo; Facilidade de desenvolvimento de testes;
		 * Redução do acoplamento; Complexidade reduzida; Coesão das classes.
		 * 
		 * 
		 * O = Open Closed Principie.
		 * Ser capaz de modificar uma classe para cada novo comportamento que ela precise ter.
		 * Deve ser capaz de entender o comportamento de uma classe sem precisar modificá-la.
		 * 
		 * 
		 * L = Liskov substitution Principie.
		 * As classes derivadas devem poder ser substituídas por sua classe base.
		 * Todas as implementações dos métodos da classe pai devem fazer sentido para as classes filhas.
		 * As condições de entradas dos métodos das classes filhas devem se encaixar com as da classe pai.
		 * 
		 * 
		 * I = Interface Segregation Principie.
		 * Crie interfaces granulares e específicas para seus clientes.
		 * Clientes não devem ser forçados a depender de interfaces que eles não utilizam.
		 * 
		 * 
		 * D = Dependency Inversion Principie.
		 * Depender de abstrações e não de implementações.
		 * Depender de abstrações faz com que seu código tenha menos dependências e diminui o acoplamento.
		 * 
		 * 
		 */

	}

}
