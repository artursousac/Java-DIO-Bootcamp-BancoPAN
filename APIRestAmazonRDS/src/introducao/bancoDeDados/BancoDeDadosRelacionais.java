package introducao.bancoDeDados;

public class BancoDeDadosRelacionais {

	public static void main(String[] args) {
		/*
		 * Objetivo do banco de dados é trazer uma persistência de dados
		 * com estruturação de dados e forneça um retrato fiel da realidade.
		 * Este retrato fiel é o contexto do mundo real.
		 * Aspectos dos dados vem com regras para corretude e um acesso controlado.
		 * Fatores de influência nos dados podem ser o volume, a velocidade e a heterogeniedade.
		 * Inicialmente, supomos que os dados são espalhados e, por meio de uma classificação
		 * criteriosa, sejam organizados.
		 * Banco de dados fornece controle de acesso concorrente ou bloqueio, além da parte de
		 * autorização.
		 * Dados = Contextos. Regras = Controle de acesso e autorização. Operações = Em cima dos dados.
		 * Banco de dados podem ser entendidos como mundo fechado.
		 * Motivos para utilizar banco de dados é a padronização com SQL, o isolamento dos dados (lógico),
		 * a consistência de estado, as regras de integridade, os mecanismos de acesso (estrutura,
		 * gerenciamento de memória, processamento...).
		 * Modelo relacional é baseado na teoria de conjuntos com tabelas bidimensionais
		 * e uma relação entre conjuntos (objetos). Funciona como um snapshot, uma foto naquele momento.
		 * Tabelas = objetos. Colunas = Atributos. Linhas = Registros do banco de dados.
		 * Benefícios de BDs relacionais podem ser a simplicidade na consulta, a vasta utilização
		 * os dados estruturados e a consistência dos dados.
		 */
	}

}
